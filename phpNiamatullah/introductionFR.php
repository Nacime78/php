        Le PHP(Hypertext Preprocessor) est un langage de programmation (fonctionnel) à l'inverse des langages HTML et CSS qui sont des langages de conception (structure et mise en forme de page web).
        Avec PHP, nous pouvons faire tout type de site et il n'y a plus de limite :
        moteur de recherche(Google), réseaux sociaux(Facebook), plateforme multimédia(Youtube), site d'informations(Wikipedia), boutique e-commerce(Amazon), petites annonces(Leboncoin), forum, blog, espace membre, etc.

        Exemple : 
        1 formulaire est declaré en HTML,
        il est mis en forme en css(HTML + CSS = conception)
        mais si nous voulons récupérer et exploiter les saisies des traitements (nous aurons besoin du php qui est un langage permettant d'effectuer des traitements des données !).
        Sans PHP nous ne pourrions pas faire fonctionner un formulaire.
        Ensuit, si nous voulons sauvegarder ces données dans une base, nous aurons besoin du langage SQL.

        Le php permet de créer des sites web dynamiques! Et c'est trés utile
